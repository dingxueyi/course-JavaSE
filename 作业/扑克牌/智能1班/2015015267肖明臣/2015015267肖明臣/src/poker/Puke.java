package poker;

public class Puke {
	private String flowerColor;
	private int value;
	public Puke(String s,int b){
		flowerColor=s;
		value=b;
	}

	public String getFlowerColor() {
		return flowerColor;
	}
	public void setFlowerColor(String flowerColor) {
		this.flowerColor = flowerColor;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
}
